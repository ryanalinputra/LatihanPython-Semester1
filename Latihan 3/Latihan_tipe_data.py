# Mencetak karakter
print ("Rudi Rusdiansyah")

# Mencetak angka/integer
print (123456)

# Mencetak karakter dan angka
print ("Rudi Rusdiansyah", 123456)

# Tipe data Boolean
print(True)

# Tipe data String
print("Ayo belajar Python")

# Tipe data Integer
print(20)

# Tipe data Float
print(3.14)

# Tipe data Hexadecimal
print(0x9a)

# Tipe data Complex
print(5j)

# Tipe data List
print([1, "dua", 3, "empat", 5])

# Tipe data Tuple
print(("satu", 2, "tiga", 4, "lima"))

# Tipe data Dictionary
print({"nama":"Budi", "umur":20})

# Tipe data Dictionary dimasukan ke dalam variabel biodata
biodata = {"nama":"Andi", "umur":20}  # Proses inisialisasi variabel biodata
print(biodata)

# Proses pencetakan variabel biodata yang berisi tipe data Dictionary
print(type(biodata))

"""fungsi untuk mengecek jenis tipe data akan tampil <class 'dict'>
yang berarti dict adalah tipe data dictionary"""
