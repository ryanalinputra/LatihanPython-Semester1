# Operator Aritmatika
# Penjumlahan
print("Hasil 13 + 2 : ", 13 + 2)
apel = 13
jeruk = 6
buah = apel + jeruk
print ("Jumlah buah : ", buah)

# Pengurangan
hutang = 1000000
bayar = 45000
sisaHutang = hutang - bayar
print ("Hutang yang tersisa adalah : ", sisaHutang)

# Perkalian 
panjang = 20
lebar = 13
hasil = panjang * lebar
print ("Hasil dari luas persegi panjang adalah : ", hasil)

# Pembagian 
kue = 15
anak = 5
kebagianKue = kue / anak
print ("Setiap anak akan kebagian :", kebagianKue, "Kue")

# Sisa Bagi
permen = 13
bocil = 3
sisaPermen = permen % bocil
print ("Sisa permen yang telah di bagikan : ", sisaPermen)

# Pangkat 
virus = 10
jam = 3
pertumbuhanVirus = virus ** jam
print ("Setiap jam virus akan berkembang dua kali lipat, jumlah bateri setelah 3 jam adalah : ", pertumbuhanVirus)

# Pembagian bilangan bulat
rata_rata = 80
jumlahSiswa = 12
nilaiSiswa = rata_rata // jumlahSiswa
print ("Nilai setiap siswa adalah :", nilaiSiswa)