buah = ["pisang", "mangga", "semangka"]
# Menggunakan operator 'in'
print("Mangga in list buah : ", "mangga" in buah)
print("Jeruk in list buah : ", "jeruk" in buah)
# Menggunakan operator 'not in'
print("Jeruk not in list buah : ", "jeruk" not in buah)
kalimat = "Hello World"
# Memeriksa karakter atau kata dalam string
print("Huruf L in Hello Word : ", "L" in kalimat)
print("Huruf W not in Hello Word : ", "W" not in kalimat)