#Sama dengan
a = 5 # adalah assigment
print("nilai a=", a)

#Tambah sama dengan
a += 10 # artinya adalah a = a + 10
print("nilai a+= 10, maka hasil nilai a adalah", a)

#Kurang sama dengan
a -= 3 # artinya adalah a = a - 3
print("nilai a-= 3, maka hasil nilai a adalah", a)

#Kali sama dengan
a *= 2 # artinya adalah a = a * 2
print("nilai a*= 2, maka hasil nilai a adalah", a)

#Bagi sama dengan
a /= 5 # artinya adalah a = a / 5
print("nilai a/= 5, maka hasil nilai a adalah", a)

#Sisa bagi sama dengan
a %= 2 # artinya adalah a = a % 2
print("nilai a%= 2, maka hasil nilai a adalah", a)

#Pangkat sama dengan
a *= 3 # artinya adalah a = a * 3
print("nilai a**= 3, maka hasil nilai a adalah", a)

#Pembagian bulat sama dengan
a //= 2 # artinya adalah a = a // 2
print("nilai a//= 2, maka hasil nilai a adalah", a)