nilai = 9

# Jika kondisi TRUE, pernyataan dibawahnya dieksekusi
if (nilai > 7):  # Kondisi
    # Kondisi dieksekusi
    print("Nilai Lebih Besar Dari Angka Tujuh")

# Jika kondisi FALSE, pernyataan dibawahnya tidak dieksekusi
if (nilai > 10):  # Kondisi
    # Kondisi tidak dieksekusi
    print("Nilai Lebih Besar Dari Angka Sepuluh")