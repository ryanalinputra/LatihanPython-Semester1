# Jika kondisi TRUE, pernyataan dibawahnya dieksekusi
if True: # Kondisi
    print('Kode program ini akan dieksekusi')

# Jika kondisi FALSE, pernyataan dibawahnya tidak dieksekusi
if False: # Kondisi
    print('Kode program ini tidak akan dieksekusi')

# Karena bukan kondisi maka pernyataan di bawah ini akan selalu dieksekusi
print('Kode program ini akan selalu dieksekusi \
karena tidak termasuk pada percabangan')