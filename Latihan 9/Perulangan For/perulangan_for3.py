# Perulangan dari 0 sampai 3
for i in range(4):
    print("Perulangan ke -", i)

# Perulangan dari 1 sampai 3  
for i in range(1, 4):
    print("Perulangan meningkat -", i)

# Perulangan dari 1 sampai 3 dengan step 2
for i in range(1, 4, 2):
    print("Perulangan interval -", i)

# Perulangan menurun dari 4 sampai 2
for i in range(4, 1, -1):
    print("Perulangan menurun -", i)